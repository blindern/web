$file = null; $desc = null;
foreach ($lines as $line)
{
	if (substr($line, 0, 1) == "\t")
	{
		if (!$file) continue;
		$desc .= ($desc ? "\n" : "") . trim($line);
	}
	
	else
	{
		if ($file)
		{
			$images[] = array(
				$file,
				$desc
			);
		}
		
		$file = trim($line);
		$desc = null;
	}
}


